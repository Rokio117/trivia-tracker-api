DROP TABLE IF EXISTS trivia_results;

DROP TYPE IF EXISTS outcome;
CREATE TYPE outcome AS ENUM (
  'Win',
  'Loss'
)

CREATE TABLE IF NOT EXISTS trivia_results(
  id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  team_id INTEGER REFERENCES trivia_teams(id) NOT NULL,
  event_id INTEGER REFERENCES trivia_eventsIid NOT NULL,
  winnings INTEGER NOT NULL,
  outcome outcome NOT NULL,
  position TEXT NOT NULL
)